var _lang = localStorage.getItem('mdLang') || 'en';

function t(key) {
  var str = (STRINGS[_lang] || STRINGS.en)[key] || STRINGS.en[key] || key;
  if (typeof str === 'function') {
    var args = Array.prototype.slice.call(arguments, 1);
    return str.apply(null, args);
  }
  return str;
}

function setLang(lang) {
  if (!STRINGS[lang]) return;
  _lang = lang;
  localStorage.setItem('mdLang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var k = el.getAttribute('data-i18n');
    if (STRINGS[_lang][k]) el.textContent = STRINGS[_lang][k];
  });
}

function getLang() { return _lang; }
