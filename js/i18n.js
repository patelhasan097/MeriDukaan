// ── Active language ──────────────────────────────────────────────────────
let _lang = localStorage.getItem('mdLang') || 'en';

export function setLang(lang) {
  if (!strings[lang]) return;
  _lang = lang;
  localStorage.setItem('mdLang', lang);
  document.documentElement.lang = lang;
  // Re-render all static text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (strings[_lang][key]) el.textContent = strings[_lang][key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (strings[_lang][key]) el.placeholder = strings[_lang][key];
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (strings[_lang][key]) el.setAttribute('aria-label', strings[_lang][key]);
  });
}

export function getLang() { return _lang; }

/** Main translation function. Usage: t('sale_saved')  or  t('best_day', day, pct) */
export function t(key, ...args) {
  const str = strings[_lang]?.[key] ?? strings['en']?.[key];
  if (!str) return key;
  if (typeof str === 'function') return str(...args);
  return str;
}

